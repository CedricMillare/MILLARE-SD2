// Mini-activity1
// Using prompt convert Celsius to Kelvin
// Present the output using alert

let celsius = parseInt(prompt("Enter the temperature in Celsius: "));
let kelvin = 273.15;
alert("Temperature in Kelvin: " + (celsius + kelvin));




